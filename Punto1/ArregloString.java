package Punto1;


/**
 * Clase que muestra un ejemplo de un arreglo de tipo String en Java.
 */
public class ArregloString {

    /**
     * Punto de entrada para la ejecución del programa.
     */
    public static void main(String[] args) {

        // Declaración e inicialización del arreglo de tipo String con nombres
        String[] nombres = {"Carlos", "Juan", "Susana", "Pablo", "Andrés"};

        // Iteración a través de los elementos del arreglo e impresión de cada uno
        for (String dimension : nombres) {
            System.out.println(dimension);
        }
    }
}
